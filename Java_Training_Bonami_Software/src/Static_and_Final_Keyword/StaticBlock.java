package Static_and_Final_Keyword;

public class StaticBlock {
	
	/*
	 *  static block is defined as that the block which is declared with keyword static
	 *  this block is is loaded only once in class static area and it is executed at same
	 *  time when it is loaded.
	 *  
	 *  There can be number of static block in one class and the execution of block depeds 
	 *  on the sequence in which it is declared.
	 */
	
	static {
		System.out.println("Hello everyone my name is prince kumar");
		System.out.println("I am a software developer");
	}

	public static void main(String[] args) {
		
		  System.out.println("I am a golaaaaaa");
        
	}
	static 
	{
		System.out.println("Hi everyone i am a java developer");
	}

}
